// 문항 순서 생성 (PAIOR 설계 철학)
// - 1~40번: AIOR 문항 A-I-O-R 순서 교차 배치 (A1,I1,O1,R1, A2,I2,O2,R2, ...)
// - 41~130번: P-역동 P1~P9 요인별 10문항씩 순차 배치
// - V 4문항: 30, 60, 90, 120번 지점에 삽입
function buildQuestionOrder() {
  const order = [];

  // 1-29: AIOR 교차 (A-I-O-R 순서: A1,I1,O1,R1 ... A7,I7,O7,R7, A8)
  for (let i = 0; i < 7; i++) {
    for (const factor of ['A', 'I', 'O', 'R']) {
      order.push({ type: 'AIOR', factor, num: i + 1 });
    }
  }
  order.push({ type: 'AIOR', factor: 'A', num: 8 });

  // 30번 지점: V1
  order.push({ type: 'V', num: 1 });

  // 31-40: AIOR 잔여 (I8,O8,R8, A9,I9,O9,R9, A10)
  order.push({ type: 'AIOR', factor: 'I', num: 8 });
  order.push({ type: 'AIOR', factor: 'O', num: 8 });
  order.push({ type: 'AIOR', factor: 'R', num: 8 });
  for (let i = 9; i <= 10; i++) {
    for (const factor of ['A', 'I', 'O', 'R']) {
      order.push({ type: 'AIOR', factor, num: i });
    }
  }

  // 41-59: P1(10) + P2(9)
  for (let i = 0; i < 10; i++) order.push({ type: 'P', factor: '고군분투', num: i + 1 });
  for (let i = 0; i < 9; i++) order.push({ type: 'P', factor: '반항', num: i + 1 });

  // 60번 지점: V2
  order.push({ type: 'V', num: 2 });

  // 61-89: P2(1) + P3(10) + P4(10) + P5(8)
  order.push({ type: 'P', factor: '반항', num: 10 });
  for (let i = 0; i < 10; i++) order.push({ type: 'P', factor: '완벽', num: i + 1 });
  for (let i = 0; i < 10; i++) order.push({ type: 'P', factor: '외고집', num: i + 1 });
  for (let i = 0; i < 8; i++) order.push({ type: 'P', factor: '잡념', num: i + 1 });

  // 90번 지점: V3
  order.push({ type: 'V', num: 3 });

  // 91-119: P5(2) + P6(10) + P7(10) + P8(7)
  for (let i = 8; i < 10; i++) order.push({ type: 'P', factor: '잡념', num: i + 1 });
  for (let i = 0; i < 10; i++) order.push({ type: 'P', factor: '만족', num: i + 1 });
  for (let i = 0; i < 10; i++) order.push({ type: 'P', factor: '적당', num: i + 1 });
  for (let i = 0; i < 7; i++) order.push({ type: 'P', factor: '의존', num: i + 1 });

  // 120번 지점: V4
  order.push({ type: 'V', num: 4 });

  // 121-134: P8(3) + P9(10)
  for (let i = 7; i < 10; i++) order.push({ type: 'P', factor: '의존', num: i + 1 });
  for (let i = 0; i < 10; i++) order.push({ type: 'P', factor: '싫증', num: i + 1 });

  return order;
}

export const QUESTION_ORDER = buildQuestionOrder();

/**
 * 문항 순서대로 question ID 배열 반환 (q1~q134 매핑용)
 * 구글 시트 개별 문항 열(q1~qN)과 대응
 */
export function getOrderedQuestionIds() {
  const ids = []
  for (const item of QUESTION_ORDER) {
    if (item.type === 'V') ids.push(`V${item.num}`)
    else if (item.type === 'AIOR') ids.push(`${item.factor}${item.num}`)
    else ids.push(`P_${item.factor}_${item.num}`)
  }
  return ids
}

// 대상별 문항 텍스트 반환
export function getQuestions(target) {
  const isElementary = target === '초등';
  const isCollege = target === '대학생(대학/대학원)';
  const isAdult = target === '성인' || target === '성인/일반';
  const aoirA = isElementary ? AOIR_A_ELEMENTARY : AOIR_A_MIDDLE;
  const aoirO = isElementary ? AOIR_O_ELEMENTARY : AOIR_O_MIDDLE;
  const aoirI = isElementary ? AOIR_I_ELEMENTARY : AOIR_I_MIDDLE;
  const aoirR = isElementary ? AOIR_R_ELEMENTARY : AOIR_R_MIDDLE;
  const pData = isElementary ? P_ELEMENTARY : P_MIDDLE;
  const vKey = target === '초등' ? '초등' : isCollege ? '대학생' : isAdult ? '성인' : '중고등';
  const vItems = V_ITEMS[vKey];

  const questions = [];
  let vIdx = 0;

  for (const item of QUESTION_ORDER) {
    if (item.type === 'V') {
      questions.push({
        id: `V${item.num}`,
        type: 'V',
        text: vItems[vIdx].text,
        expected: vItems[vIdx].expected,
        orderIndex: questions.length
      });
      vIdx++;
    } else if (item.type === 'AIOR') {
      const arr = { A: aoirA, O: aoirO, I: aoirI, R: aoirR }[item.factor];
      questions.push({
        id: `${item.factor}${item.num}`,
        type: 'AIOR',
        factor: item.factor,
        text: arr[item.num - 1],
        orderIndex: questions.length
      });
    } else {
      questions.push({
        id: `P_${item.factor}_${item.num}`,
        type: 'P',
        factor: item.factor,
        text: pData[item.factor][item.num - 1],
        orderIndex: questions.length
      });
    }
  }

  return questions;
}

